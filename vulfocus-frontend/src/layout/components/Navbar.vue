<template>
  <div class="navbar">

    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />
    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img :src="avatar+'?imageView2'" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              主页
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item divided>
            <span style="display:block;" @click="updatePwd">修改密码</span>
          </el-dropdown-item>
          <el-dropdown-item divided>
            <span style="display:block;" @click="logout">退出</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
      <el-dialog :visible.sync="updateDialog" style="width: 26%;height: 45%;margin: auto" :modal="false" :fullscreen="true" title="修改密码">
        <el-form :model="ruleForm"  class="confirm-from" status-icon :rules="rules" ref="ruleForm" auto-complete="on" label-position="left" >
          <el-form-item prop="pass">
            <el-input
                placeholder="输入新密码"
                v-model="ruleForm.pass"
                type="password"
                style="width:70%"
                autocomplete="off"
              />
          </el-form-item>
          <el-form-item prop="checkPass">
            <el-input
                placeholder="确认密码"
                v-model="ruleForm.checkPass"
                type="password"
                style="width:70%"
                autocomplete="off"
              />
          </el-form-item>
          <el-form-item style="margin-top: 5px">
            <el-button type="primary" @click="handleUpdatePwd">提交</el-button>
          </el-form-item>
        </el-form>
      </el-dialog>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import { lininfo } from "@/api/docker"
import { updatePassword } from "@/api/user"
export default {
  data(){
    const validatePass = (rule, value, callback) => {
        if (value === '') {
          callback(new Error('请输入密码'));
        } else {
          if (this.ruleForm.checkPass !== '') {
            this.$refs.ruleForm.validateField('checkPass');
          }
          callback();
        }
      };
    const validatePass2 = (rule, value, callback) => {
      if (value === '') {
        callback(new Error('请再次输入密码'));
      } else if (value !== this.ruleForm.pass) {
        callback(new Error('两次输入密码不一致!'));
      } else {
        callback();
      }
    };
    return {
      ruleForm: {
        pass:'',
        checkPass:''
      },
      rules: {
        pass: [
          { validator: validatePass, trigger: 'blur' }
        ],
        checkPass: [
          { validator: validatePass2, trigger: 'blur' }
        ],
      },
      updateDialog:false
    }
  },
  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'name'
    ])
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    },
    updatePwd() {
      this.updateDialog = true
    },
    handleUpdatePwd() {
      this.$refs.ruleForm.validate(valid => {
        if (valid) {
          updatePassword(this.ruleForm).then(response => {
            let data = response.data
            if (data.code === 200) {
              this.$message({
                message: '修改密码成功',
                type: "success",
              })
              this.updateDialog = false
            } else {
              this.$message({
                message: data.msg,
                type: "error",
              })
            }
          })
        } else {
          return false
        }
      })
    }
  }
}
</script>

